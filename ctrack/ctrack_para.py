def ret_dlwbin():
  #wmin   = -600.0  #[hPa/day]
  #wmax   = 100.0   #[hPa/day]
  wmin   = -100.0  #[hPa/day]
  wmax   = 600.0   #[hPa/day]

  dw     = 50.0    #[hPa/day]
  l  = arange( wmin, wmax+1, dw)
  n  = len(l)
  dlwbin  = {}
  dlwbin[0] = [-10.0e+10, 10.0e+10]
  i = 1
  #-----
  for w in l:
    i = i+1
    dlwbin[i] = [w, (w+dw)]
  #-----
  dlwbin[1] = [-10.0e+10, wmin]
  dlwbin[n+1] = [wmax, 10.0e+10]
  #-----
  return dlwbin
